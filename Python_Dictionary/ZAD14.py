#15. Write a Python program to get the maximum and minimum values of a dictionary.

# Przykładowy słownik
my_dict = {'apple': 26, 'banana': 18, 'cherry': 35, 'date': 21}

# Maksymalna i minimalna wartość
max_value = max(my_dict.values())
min_value = min(my_dict.values())

print("Maksymalna wartość w słowniku:", max_value)
print("Minimalna wartość w słowniku:", min_value)